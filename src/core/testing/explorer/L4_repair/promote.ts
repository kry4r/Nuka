// src/core/testing/explorer/L4_repair/promote.ts
//
// M5.T4 — write the post-repair regression fixture.
// See locked spec §4.6 step 4.
//
// The promoted file at <out>/<component>/regression-<id>.fixtures.tsx is a
// thin shim that re-imports the original fixture (the one the dump
// points at) and re-exports a single case clamped to the failing
// viewport. CI then runs this fixture as a normal *.fixtures.tsx case;
// any regression of the original bug re-trips the original invariant
// against the same viewport, but now under a stable, named guard.

import { mkdirSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import type { FailureRecord } from '../types'

export type PromoteOpts = {
  failure: FailureRecord
  /** Output root — promoted file lives at <root>/<component>/regression-<id>.fixtures.tsx. */
  outDir: string
  /** Original dump path (for the header comment audit trail). */
  dumpPath: string
  /** One-line root cause from the subagent's final summary. */
  rootCause: string
}

export type PromoteResult = {
  /** Absolute path of the written regression fixture. */
  fixturePath: string
}

/**
 * Write the regression fixture. Returns the absolute path of the new
 * file. The destination directory is created if missing.
 *
 * @throws if `failure.fixturePath` is unset (we have nothing to import).
 */
export function promote(opts: PromoteOpts): PromoteResult {
  const { failure, outDir, dumpPath, rootCause } = opts

  if (!failure.fixturePath) {
    throw new Error(
      'promote: failure.fixturePath is unset — cannot derive an import for the regression fixture',
    )
  }

  const componentDir = path.join(outDir, failure.component)
  mkdirSync(componentDir, { recursive: true })

  const filePath = path.join(
    componentDir,
    `regression-${failure.id}.fixtures.tsx`,
  )

  // Compute the import specifier from the regression file to the
  // original fixture. Path.relative gives a POSIX-or-platform string;
  // normalise to forward-slashes for the ES module specifier.
  const relImport = path
    .relative(componentDir, failure.fixturePath)
    .split(path.sep)
    .join('/')
  const importSpec = relImport.startsWith('.') ? relImport : './' + relImport

  const body = renderRegressionSrc({
    failure,
    importSpec,
    dumpPath,
    rootCause,
  })
  writeFileSync(filePath, body, 'utf8')

  return { fixturePath: filePath }
}

function renderRegressionSrc(args: {
  failure: FailureRecord
  importSpec: string
  dumpPath: string
  rootCause: string
}): string {
  const { failure, importSpec, dumpPath, rootCause } = args
  const safeRule = failure.violations[0]?.rule ?? 'unknown'
  // Strip the .tsx extension from the import (TS ESM imports omit it).
  const importNoExt = importSpec.replace(/\.tsx?$/, '')
  return [
    `// AUTO-GENERATED by ink-ui-explorer M5 repair pipeline.`,
    `//`,
    `// Source dump: ${dumpPath}`,
    `// Root cause: ${rootCause.split('\n')[0]}`,
    `// First violation: ${safeRule}`,
    `// Failing viewport: ${failure.viewport.cols}×${failure.viewport.rows}`,
    `//`,
    `// This fixture mounts the same case the original dump observed at the`,
    `// same viewport. Any regression of the bug re-fires the invariant.`,
    ``,
    `import original from '${importNoExt}'`,
    `import type { FixtureDef } from '../../../../src/core/testing/explorer/types'`,
    ``,
    `const sourceCase = original.cases[${JSON.stringify(failure.fixtureCase)}]`,
    `if (!sourceCase) {`,
    `  throw new Error(`,
    `    'regression-${failure.id}: case ${failure.fixtureCase} missing from original fixture',`,
    `  )`,
    `}`,
    ``,
    `const fixture: FixtureDef = {`,
    `  component: ${JSON.stringify(failure.component)},`,
    `  cases: {`,
    `    [${JSON.stringify(`regression_${failure.id}`)}]: sourceCase,`,
    `  },`,
    `  viewports: [{ cols: ${failure.viewport.cols}, rows: ${failure.viewport.rows} }],`,
    `}`,
    ``,
    `export default fixture`,
    ``,
  ].join('\n')
}
